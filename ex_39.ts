function cityCountry(city: string, country: string): string {
    return `${city}, ${country}`;
}

// Example usage:
const city1 = cityCountry("Lahore", "Pakistan");
const city2 = cityCountry("Tokyo", "Japan");
const city3 = cityCountry("Barcelona", "Spain");

console.log(city1); // Lahore, Pakistan
console.log(city2); // Tokyo, Japan
console.log(city3); // Barcelona, Spain
