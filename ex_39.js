function cityCountry(city, country) {
    return "".concat(city, ", ").concat(country);
}
// Example usage:
var city1 = cityCountry("Lahore", "Pakistan");
var city2 = cityCountry("Tokyo", "Japan");
var city3 = cityCountry("Barcelona", "Spain");
console.log(city1); // Lahore, Pakistan
console.log(city2); // Tokyo, Japan
console.log(city3); // Barcelona, Spain
